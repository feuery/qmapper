template <class T1, class T2>
std::unordered_map<T2, T1> swapPairs(std::unordered_map<T1, T2> m) {
    std::unordered_map<T2, T1> m1;

    for (auto&& item : m) {
        m1.emplace(item.second, item.first);
    }

    return m1;
};

